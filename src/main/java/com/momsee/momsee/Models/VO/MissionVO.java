package com.momsee.momsee.Models.VO;

public class MissionVO {
    String email;
    String child_name;
    String mission_cont;
    int mission_time;
    String mission_image;

    public String getEmail() {
        return email;
    }

    public void setEmail(String email) {
        this.email = email;
    }

    public String getChild_name() {
        return child_name;
    }

    public void setChild_name(String child_name) {
        this.child_name = child_name;
    }

    public String getMission_cont() {
        return mission_cont;
    }

    public void setMission_cont(String mission_cont) {
        this.mission_cont = mission_cont;
    }

    public int getMission_time() {
        return mission_time;
    }

    public void setMission_time(int mission_time) {
        this.mission_time = mission_time;
    }

    public String getMission_image() {
        return mission_image;
    }

    public void setMission_image(String mission_image) {
        this.mission_image = mission_image;
    }

}
